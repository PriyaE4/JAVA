package Programs;
import java.util.*;
public class StandardMethods {
	public static void main(String[] args) {
		int n= 64;
		String s = "I am HAPPY today.";
		System.out.println("The length of the string is: "+(s.length()));
		System.out.println("After changing all to uppercase: "+(s.toUpperCase()));
		System.out.println("After changing all to lowercase: "+(s.toLowerCase()));
		System.out.println("The square root of the given number is: "+(Math.sqrt(n)));
		System.out.println("The square of the given number is: "+(Math.pow(n,2)));
	}

}
